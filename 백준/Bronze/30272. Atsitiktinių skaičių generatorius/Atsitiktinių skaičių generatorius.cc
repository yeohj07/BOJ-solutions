#include <bits/stdc++.h>
#define int long long
using namespace std;

int32_t main() {
    cin.tie(0)->sync_with_stdio(0);

    vector<string> v = {"..#####..", "....##...", ".#######.", ".#######.", "##.......",
                         ".########", ".#######.", ".########", ".#######.", ".#######."};
    vector<string> u = {"##.....##", "....##...", ".#######.", ".#######.", "##....##.",
                         ".#######.", "########.", "....##...", ".#######.", ".########"};
    vector<string> vv = {"##.....##", "....##...", "##.......", ".......##", "#########",
                          ".......##", "##.....##", "...##....", "##.....##", ".......##"};

    int N; cin >> N;

    string ans = "";

    while(N--) {
        vector<string> w(8);

        for(int i=0; i<8; i++) cin >> w[i];

        for(int i=0; i<10; i++)
            if(v[i] == w[0] && u[i] == w[3] && vv[i] == w[4]) ans += char('0' + i);
    }

    cout << ans << "\n";
}
